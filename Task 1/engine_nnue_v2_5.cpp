int nmp_reduction(int depth)
{
    if (!NMP_ADAPTIVE) return NMP_BASE_R;
    return (depth <= 4) ? 2 : 3;
}

bool nmp_is_safe(const BoardState &st, bool playing_white, int alpha, int beta)
{
    if (is_in_check(st, playing_white)) return false;
    int wm = 0, bm = 0;
    for (int sq = 0; sq < 36; ++sq)
    {
        int p = st.squares[sq];
        if (p == W_KNIGHT || p == W_BISHOP || p == W_QUEEN) wm += std::abs(PIECE_VALUES[p]);
        else if (p == B_KNIGHT || p == B_BISHOP || p == B_QUEEN) bm += std::abs(PIECE_VALUES[p]);
    }
    if (wm < NMP_MINOR_THRESHOLD && bm < NMP_MINOR_THRESHOLD) return false;
    int stat = evaluate(st);
    if (playing_white) return stat >= beta + NMP_MARGIN;
    return stat <= alpha - NMP_MARGIN;
}

int static_exchange_evaluation(const BoardState &st, int sr, int sc, int dr, int dc)
{
    int dest_sq = sq_index(dr, dc);
    int piece_captured = st.squares[dest_sq];
    if (piece_captured == EMPTY) return 0;
    int piece_moved = st.squares[sq_index(sr, sc)];
    bool is_w = is_white(piece_moved);

    int enemies = 0, allies = -1;
    auto tally = [&](int p) { if (is_white(p) == is_w) allies++; else enemies++; };
    auto at = [&](int r, int c) -> int
    {
        if (!in_bounds(r, c)) return EMPTY;
        return st.squares[sq_index(r, c)];
    };

    for (auto &d : ORTHO_DIRS)
    {
        int r = dr + d[0], c = dc + d[1];
        while (in_bounds(r, c))
        {
            int p = at(r, c);
            if (p != EMPTY) { if (p == W_QUEEN || p == B_QUEEN) tally(p); break; }
            r += d[0]; c += d[1];
        }
    }
    for (auto &d : DIAG_DIRS)
    {
        int r = dr + d[0], c = dc + d[1];
        while (in_bounds(r, c))
        {
            int p = at(r, c);
            if (p != EMPTY) { if (p == W_BISHOP || p == B_BISHOP || p == W_QUEEN || p == B_QUEEN) tally(p); break; }
            r += d[0]; c += d[1];
        }
    }
    for (auto &d : KNIGHT_DIRS)
    {
        int r = dr + d[0], c = dc + d[1];
        if (!in_bounds(r, c)) continue;
        int p = at(r, c);
        if (p == W_KNIGHT || p == B_KNIGHT) tally(p);
    }
    for (int c_dir : {-1, 1})
    {
        if (in_bounds(dr - 1, dc + c_dir)) { int p = at(dr - 1, dc + c_dir); if (p == W_PAWN) tally(p); }
        if (in_bounds(dr + 1, dc + c_dir)) { int p = at(dr + 1, dc + c_dir); if (p == B_PAWN) tally(p); }
    }
    for (int dr2 = -1; dr2 <= 1; ++dr2)
        for (int dc2 = -1; dc2 <= 1; ++dc2)
        {
            if (dr2 == 0 && dc2 == 0) continue;
            int r = dr + dr2, c = dc + dc2;
            if (!in_bounds(r, c)) continue;
            int p = at(r, c);
            if (p == W_KING || p == B_KING) tally(p);
        }

    if (enemies > allies) return -std::abs(PIECE_VALUES[piece_captured]);
    return std::abs(PIECE_VALUES[piece_captured]);
}

int score_move_for_ordering(uint32_t m, const BoardState &st, int ply, uint32_t tt_move)
{
    int from = move_from(m), to = move_to(m);
    int piece = move_piece(m), promo = move_promo(m);
    int target = st.squares[to];

    if (tt_move && m == tt_move) return 2'000'000;
    if (target != EMPTY)
    {
        int see_score = static_exchange_evaluation(st, sq_row(from), sq_col(from), sq_row(to), sq_col(to));
        if (see_score < 0) return 1000 + see_score;
        return 1'000'000 + (10 * std::abs(PIECE_VALUES[target]) - std::abs(PIECE_VALUES[piece])) + see_score;
    }
    if (promo) return 950'000;
    int clamped = std::min(ply, 63);
    if (m == KILLER_MOVES[clamped][0]) return 900'000;
    if (m == KILLER_MOVES[clamped][1]) return 800'000;
    int r = sq_row(to), c = sq_col(to);
    return std::min(HISTORY_TABLE[piece][r][c], 700'000);
}

