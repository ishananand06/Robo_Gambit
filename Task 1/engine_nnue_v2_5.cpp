using Clock = std::chrono::steady_clock;

int quiescence(BoardState &st, int alpha, int beta, bool maximizing,
               const Clock::time_point &start_t, double time_limit)
{
    if ((STATS.nodes & 255) == 0)
    {
        double elapsed = std::chrono::duration<double>(Clock::now() - start_t).count();
        if (elapsed > time_limit) throw std::runtime_error("timeout");
    }
    STATS.nodes++;

    int stand_pat = evaluate(st);
    if (maximizing)
    {
        if (stand_pat >= beta) return beta;
        if (stand_pat > alpha) alpha = stand_pat;
    }
    else
    {
        if (stand_pat <= alpha) return alpha;
        if (stand_pat < beta)  beta = stand_pat;
    }

    std::vector<uint32_t> all_moves;
    generate_legal_moves(st, maximizing, all_moves);
    std::vector<uint32_t> safe_captures;
    for (uint32_t m : all_moves)
    {
        int to = move_to(m), promo = move_promo(m);
        if (st.squares[to] == EMPTY && promo == 0) continue;
        if (promo != 0)
        {
            safe_captures.push_back(m);
        }
        else
        {
            int from = move_from(m);
            if (static_exchange_evaluation(st, sq_row(from), sq_col(from), sq_row(to), sq_col(to)) >= 0)
                safe_captures.push_back(m);
        }
    }

    std::sort(safe_captures.begin(), safe_captures.end(),
              [&](uint32_t a, uint32_t b)
              { return score_move_for_ordering(a, st, 0, 0) > score_move_for_ordering(b, st, 0, 0); });

    for (uint32_t m : safe_captures)
    {
        UndoInfo ui;
        make_move(st, m, ui);
        int val = quiescence(st, alpha, beta, !maximizing, start_t, time_limit);
        unmake_move(st, ui);
        if (maximizing)
        {
            if (val > alpha) alpha = val;
            if (alpha >= beta) return beta;
        }
        else
        {
            if (val < beta)  beta = val;
            if (beta <= alpha) return alpha;
        }
    }
    return maximizing ? alpha : beta;
}

int minimax(BoardState &st, int depth, int alpha, int beta, bool maximizing, int ply,
            const Clock::time_point &start_t, double time_limit, bool null_move_allowed)
{
    uint64_t h = st.hash;
    if (GAME_HISTORY.count(h) && GAME_HISTORY[h] >= 2) return 0;

    int orig_alpha = alpha;
    uint32_t tt_best_move = 0;

    TTEntry &entry = TT[h % TT_SIZE];
    if (entry.hash == h && entry.depth >= depth)
    {
        int adj_v = retrieve_mate_score(entry.score, ply);
        tt_best_move = entry.best_move;
        if (entry.flag == TT_EXACT) return adj_v;
        else if (entry.flag == TT_LOWER) alpha = std::max(alpha, adj_v);
        else if (entry.flag == TT_UPPER) beta  = std::min(beta,  adj_v);
        if (alpha >= beta) return adj_v;
    }

    if (depth == 0)
        return quiescence(st, alpha, beta, maximizing, start_t, time_limit);

    if ((STATS.nodes & 255) == 0)
    {
        double elapsed = std::chrono::duration<double>(Clock::now() - start_t).count();
        if (elapsed > time_limit) throw std::runtime_error("timeout");
    }
    STATS.nodes++;

    // Null move pruning — lightweight: only hash + side-to-move change,
    // no BoardState copy (avoids copying the 1 KB dual accumulator).
    if (null_move_allowed && depth >= NMP_MIN_DEPTH && nmp_is_safe(st, maximizing, alpha, beta))
    {
        int R = nmp_reduction(depth);
        int null_depth = std::max(0, depth - R - 1);
        // Save only what changes
        uint64_t saved_hash = st.hash;
        bool     saved_wtm  = st.white_to_move;
        hash_side(st);
        st.white_to_move = !st.white_to_move;
        int null_score = minimax(st, null_depth, alpha, beta, !maximizing, ply + 1, start_t, time_limit, false);
        // Restore only what changed
        st.hash          = saved_hash;
        st.white_to_move = saved_wtm;
        if ((maximizing && null_score >= beta) || (!maximizing && null_score <= alpha))
        {
            STATS.nmp_cutoffs++;
            int cutoff_val = maximizing ? beta : alpha;
            entry.hash = h; entry.depth = depth;
            entry.score = store_mate_score(cutoff_val, ply);
            entry.flag = maximizing ? TT_LOWER : TT_UPPER;
            entry.best_move = 0;
            return cutoff_val;
        }
    }

    std::vector<uint32_t> moves;
    generate_legal_moves(st, maximizing, moves);
    if (moves.empty())
    {
        int king_piece = maximizing ? W_KING : B_KING;
        int kr = -1, kc = -1;
        for (int sq = 0; sq < 36; ++sq)
            if (st.squares[sq] == king_piece) { kr = sq_row(sq); kc = sq_col(sq); break; }
        if (kr != -1 && is_attacked(st, kr, kc, !maximizing))
            return maximizing ? -(MATE_VALUE - ply) : (MATE_VALUE - ply);
        return 0;
    }

    std::sort(moves.begin(), moves.end(),
              [&](uint32_t a, uint32_t b)
              { return score_move_for_ordering(a, st, ply, tt_best_move) > score_move_for_ordering(b, st, ply, tt_best_move); });

    int best_v = maximizing ? -1000000000 : 1000000000;
    uint32_t best_move = moves[0];
    bool in_check_now = is_in_check(st, maximizing);

    for (size_t i = 0; i < moves.size(); ++i)
    {
        uint32_t m = moves[i];
        UndoInfo ui;
        make_move(st, m, ui);
        uint64_t child_hash = st.hash;
        int from = move_from(m), to = move_to(m);
        int piece = move_piece(m), promo = move_promo(m);
        int captured = ui.captured_piece;
        int dr_row = sq_row(to);
        int extension = ((piece == W_PAWN && dr_row == 4) || (piece == B_PAWN && dr_row == 1)) ? 1 : 0;

        bool is_capture  = (captured != EMPTY);
        bool is_promo    = (promo != 0);
        int ply_idx      = std::min(ply, 63);
        bool is_killer   = (m == KILLER_MOVES[ply_idx][0] || m == KILLER_MOVES[ply_idx][1]);

        bool use_lmr = (i >= (size_t)LMR_MIN_MOVE_INDEX && depth >= LMR_MIN_DEPTH &&
                        !is_capture && !is_promo && !in_check_now && !is_killer && extension == 0);

        int val;
        if (use_lmr)
        {
            // LMR reduction: capped at min(depth/3, 2) for 6x6.
            // The standard log*log/2 formula is calibrated for 8x8 and over-reduces
            // in a small-board tactical environment. Depth/3 with a hard cap of 2
            // keeps reductions modest so dense tactical sequences are not pruned away.
            int R = std::min({std::max(1, int(std::log(depth) * std::log(int(i) + 1) / 2.0)),
                              depth / 3,
                              2});
            STATS.lmr_reductions++;
            int lmr_alpha = maximizing ? alpha : beta - 1;
            int lmr_beta  = maximizing ? alpha + 1 : beta;
            GAME_HISTORY[child_hash]++;
            val = minimax(st, depth - 1 - R + extension, lmr_alpha, lmr_beta, !maximizing, ply + 1, start_t, time_limit, true);
            if ((maximizing && val > alpha) || (!maximizing && val < beta))
            {
                STATS.lmr_researches++;
                val = minimax(st, depth - 1 + extension, alpha, beta, !maximizing, ply + 1, start_t, time_limit, true);
            }
            GAME_HISTORY[child_hash]--;
        }
        else
        {
            GAME_HISTORY[child_hash]++;
            val = minimax(st, depth - 1 + extension, alpha, beta, !maximizing, ply + 1, start_t, time_limit, true);
            GAME_HISTORY[child_hash]--;
        }

        unmake_move(st, ui);

        if (maximizing)
        {
            if (val > best_v) { best_v = val; best_move = m; }
            if (val > alpha)  alpha = val;
        }
        else
        {
            if (val < best_v) { best_v = val; best_move = m; }
            if (val < beta)   beta = val;
        }

        if (beta <= alpha)
        {
            if (!is_capture)
            {
                int clamped = ply_idx;
                if (m != KILLER_MOVES[clamped][0])
                {
                    KILLER_MOVES[clamped][1] = KILLER_MOVES[clamped][0];
                    KILLER_MOVES[clamped][0] = m;
                }
                int r = sq_row(to), c = sq_col(to);
                HISTORY_TABLE[piece][r][c] += depth * depth;
            }
            STATS.cutoffs++;
            break;
        }
    }

    int flag = (best_v <= orig_alpha) ? TT_UPPER : (best_v >= beta) ? TT_LOWER : TT_EXACT;
    // Depth-preferred replacement: only overwrite if the new search is deeper
    // (or the slot belongs to a different position). This prevents a shallow
    // sweep from evicting an expensive deep result that shares the same index.
    if (entry.hash != h || entry.depth <= depth)
    {
        entry.hash      = h;
        entry.depth     = depth;
        entry.score     = store_mate_score(best_v, ply);
        entry.flag      = flag;
        entry.best_move = best_move;
    }
    return best_v;
}

