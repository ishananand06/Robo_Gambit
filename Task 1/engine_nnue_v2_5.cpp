// RoboGambit 6x6 Constants
constexpr int BOARD_SIZE = 6;
constexpr uint64_t UNIVERSE = 0x0000000FFFFFFFFFULL; // 36 bits

enum Piece
{
    EMPTY = 0,
    W_PAWN = 1,
    W_KNIGHT = 2,
    W_BISHOP = 3,
    W_QUEEN = 4,
    W_KING = 5,
    B_PAWN = 6,
    B_KNIGHT = 7,
    B_BISHOP = 8,
    B_QUEEN = 9,
    B_KING = 10
};

inline bool is_white(int p) { return p >= W_PAWN && p <= W_KING; }
inline bool is_black(int p) { return p >= B_PAWN && p <= B_KING; }
inline bool same_side(int p1, int p2)
{
    if (p1 == EMPTY || p2 == EMPTY)
        return false;
    return (is_white(p1) && is_white(p2)) || (is_black(p1) && is_black(p2));
}

inline int sq_index(int r, int c) { return r * BOARD_SIZE + c; }
inline int sq_row(int idx) { return idx / BOARD_SIZE; }
inline int sq_col(int idx) { return idx % BOARD_SIZE; }
inline bool in_bounds(int r, int c)
{
    return r >= 0 && r < BOARD_SIZE && c >= 0 && c < BOARD_SIZE;
}

// Piece values (for move ordering / SEE only – evaluation is NNUE)
constexpr int PIECE_VALUES[11] = {
    0,
    100, 300, 320, 900, 20000,
    -100, -300, -320, -900, -20000};

constexpr int INITIAL_COUNTS[11] = {
    0,
    6, 2, 2, 1, 1,
    6, 2, 2, 1, 1};

const int ORTHO_DIRS[4][2] = {{-1, 0}, {1, 0}, {0, -1}, {0, 1}};
const int DIAG_DIRS[4][2] = {{-1, -1}, {-1, 1}, {1, -1}, {1, 1}};
const int KNIGHT_DIRS[8][2] = {{-2, -1}, {-2, 1}, {-1, -2}, {-1, 2}, {1, -2}, {1, 2}, {2, -1}, {2, 1}};

// Zobrist
uint64_t ZOBRIST_TABLE[11][36];
uint64_t ZOBRIST_BLACK_TO_MOVE = 0;
uint64_t ZOBRIST_GRAVEYARD[11][4];

std::mt19937_64 rng_zob(42);
inline uint64_t rand64() { return rng_zob(); }

void init_zobrist()
{
    static bool init = false;
    if (init) return;
    init = true;
    for (int p = 0; p <= 10; ++p)
        for (int s = 0; s < 36; ++s)
            ZOBRIST_TABLE[p][s] = rand64();
    ZOBRIST_BLACK_TO_MOVE = rand64();
    for (int p : {W_QUEEN, W_BISHOP, W_KNIGHT, B_QUEEN, B_BISHOP, B_KNIGHT})
    {
        int maxc = INITIAL_COUNTS[p];
        for (int c = 1; c <= maxc; ++c)
            ZOBRIST_GRAVEYARD[p][c] = rand64();
    }
}

