// Move encoding
inline uint32_t encode_move(int from, int to, int piece, int promo)
{
    return (from & 63) | ((to & 63) << 6) | ((piece & 15) << 12) | ((promo & 15) << 16);
}
inline int move_from(uint32_t m)  { return m & 63; }
inline int move_to(uint32_t m)    { return (m >> 6) & 63; }
inline int move_piece(uint32_t m) { return (m >> 12) & 15; }
inline int move_promo(uint32_t m) { return (m >> 16) & 15; }

inline int store_mate_score(int s, int ply)
{
    if (s > 90000)  return s + ply;
    if (s < -90000) return s - ply;
    return s;
}
inline int retrieve_mate_score(int s, int ply)
{
    if (s > 90000)  return s - ply;
    if (s < -90000) return s + ply;
    return s;
}

inline void hash_piece(BoardState &st, int sq, int piece) { st.hash ^= ZOBRIST_TABLE[piece][sq]; }
inline void hash_side(BoardState &st) { st.hash ^= ZOBRIST_BLACK_TO_MOVE; }
inline void hash_grave(BoardState &st, int piece, int count_before, int count_after)
{
    if (count_before > 0) st.hash ^= ZOBRIST_GRAVEYARD[piece][count_before];
    if (count_after > 0)  st.hash ^= ZOBRIST_GRAVEYARD[piece][count_after];
}

// ──────────────────────────────────────────────────────────────────────────────
// Phase 3: Dual feature index mapping
//
// WHITE perspective (accumulator[0]) – absolute coords, original piece ids:
//   idx = (r * 6 + c) * 10 + (piece - 1)
//
// BLACK perspective (accumulator[1]) – canonical form matching training:
//   Flip rows:   rf = 5 - r
//   Swap colors: White pieces (1-5) → 6-10, Black pieces (6-10) → 1-5
//   idx = (rf * 6 + c) * 10 + (flipped_piece - 1)
// ──────────────────────────────────────────────────────────────────────────────

inline int get_feature_white(int r, int c, int piece)
{
    // White's absolute view – identical to the original single-accumulator formula
    if (piece == EMPTY) return -1;
    return (r * 6 + c) * 10 + (piece - 1);
}

inline int get_feature_black(int r, int c, int piece)
{
    // Black's canonical view: board flipped, own pieces always appear as 1-5
    if (piece == EMPTY) return -1;
    int rf = 5 - r;
    int pc = is_white(piece) ? (piece + 5) : (piece - 5); // swap colors
    return (rf * 6 + c) * 10 + (pc - 1);
}

// ──────────────────────────────────────────────────────────────────────────────
// Phase 4a: Accumulator helpers
// ──────────────────────────────────────────────────────────────────────────────

// Low-level: add/subtract one weight row from one accumulator
inline void acc_add(int16_t acc[256], int feature_idx)
{
    const int16_t *row = &nnue::fc1_weights[feature_idx * 256];
    #pragma GCC unroll 8
    for (int i = 0; i < 256; ++i)
        acc[i] += row[i];
}
inline void acc_sub(int16_t acc[256], int feature_idx)
{
    const int16_t *row = &nnue::fc1_weights[feature_idx * 256];
    #pragma GCC unroll 8
    for (int i = 0; i < 256; ++i)
        acc[i] -= row[i];
}

// High-level: update BOTH perspectives at once
inline void acc_add_piece(BoardState &st, int r, int c, int piece)
{
    acc_add(st.accumulator[0], get_feature_white(r, c, piece));
    acc_add(st.accumulator[1], get_feature_black(r, c, piece));
}
inline void acc_sub_piece(BoardState &st, int r, int c, int piece)
{
    acc_sub(st.accumulator[0], get_feature_white(r, c, piece));
    acc_sub(st.accumulator[1], get_feature_black(r, c, piece));
}

// ──────────────────────────────────────────────────────────────────────────────
// Phase 4b: init_board_from_array – builds accumulator from scratch
// ──────────────────────────────────────────────────────────────────────────────

void init_board_from_array(BoardState &st, int *flat_board, bool playing_white)
{
    init_zobrist();
    st.white_occupancy = st.black_occupancy = st.all_occupancy = 0;
    std::memset(st.bitboards, 0, sizeof(st.bitboards));
    std::memset(st.squares, 0, sizeof(st.squares));
    std::memset(st.graveyard, 0, sizeof(st.graveyard));
    st.hash = 0;

    // Initialise BOTH accumulators to fc1_bias
    for (int i = 0; i < 256; ++i)
        st.accumulator[0][i] = st.accumulator[1][i] = nnue::fc1_bias[i];

    int counts[11] = {0};
    for (int sq = 0; sq < 36; ++sq)
    {
        int p = flat_board[sq];
        st.squares[sq] = p;
        if (p != EMPTY)
        {
            st.bitboards[p] |= (uint64_t(1) << sq);
            if (is_white(p))
                st.white_occupancy |= (uint64_t(1) << sq);
            else
                st.black_occupancy |= (uint64_t(1) << sq);
            counts[p]++;
            hash_piece(st, sq, p);

            // Update both accumulators (White absolute + Black canonical)
            acc_add_piece(st, sq_row(sq), sq_col(sq), p);
        }
    }
    st.all_occupancy = st.white_occupancy | st.black_occupancy;
    st.white_to_move = playing_white;
    if (!playing_white)
        hash_side(st);

    for (int p = 0; p <= 10; ++p)
    {
        if (INITIAL_COUNTS[p] > 0)
        {
            int miss = INITIAL_COUNTS[p] - counts[p];
            if (miss > 0)
            {
                st.graveyard[p] = miss;
                if (p == W_QUEEN || p == W_BISHOP || p == W_KNIGHT ||
                    p == B_QUEEN || p == B_BISHOP || p == B_KNIGHT)
                    hash_grave(st, p, 0, miss);
            }
        }
    }
}

// ──────────────────────────────────────────────────────────────────────────────
// Attack / check detection (unchanged from engine.cpp)
// ──────────────────────────────────────────────────────────────────────────────

bool is_attacked(const BoardState &st, int kr, int kc, bool by_white)
{
    const int *b = st.squares;
    auto at = [&](int r, int c) -> int
    {
        if (!in_bounds(r, c)) return EMPTY;
        return b[sq_index(r, c)];
    };

    for (auto &d : ORTHO_DIRS)
    {
        int r = kr + d[0], c = kc + d[1];
        while (in_bounds(r, c))
        {
            int p = at(r, c);
            if (p != EMPTY)
            {
                if (by_white && p == W_QUEEN) return true;
                if (!by_white && p == B_QUEEN) return true;
                break;
            }
            r += d[0]; c += d[1];
        }
    }
    for (auto &d : DIAG_DIRS)
    {
        int r = kr + d[0], c = kc + d[1];
        while (in_bounds(r, c))
        {
            int p = at(r, c);
            if (p != EMPTY)
            {
                if (by_white && (p == W_BISHOP || p == W_QUEEN)) return true;
                if (!by_white && (p == B_BISHOP || p == B_QUEEN)) return true;
                break;
            }
            r += d[0]; c += d[1];
        }
    }
    for (auto &d : KNIGHT_DIRS)
    {
        int r = kr + d[0], c = kc + d[1];
        if (!in_bounds(r, c)) continue;
        int p = at(r, c);
        if (by_white && p == W_KNIGHT) return true;
        if (!by_white && p == B_KNIGHT) return true;
    }
    int pawn_dr = by_white ? -1 : 1;
    for (int dc : {-1, 1})
    {
        int r = kr + pawn_dr, c = kc + dc;
        if (!in_bounds(r, c)) continue;
        int p = at(r, c);
        if (by_white && p == W_PAWN) return true;
        if (!by_white && p == B_PAWN) return true;
    }
    for (int dr = -1; dr <= 1; ++dr)
        for (int dc = -1; dc <= 1; ++dc)
        {
            if (dr == 0 && dc == 0) continue;
            int r = kr + dr, c = kc + dc;
            if (!in_bounds(r, c)) continue;
            int p = at(r, c);
            if (by_white && p == W_KING) return true;
            if (!by_white && p == B_KING) return true;
        }
    return false;
}

bool is_in_check(const BoardState &st, bool checking_white)
{
    int king_piece = checking_white ? W_KING : B_KING;
    for (int sq = 0; sq < 36; ++sq)
        if (st.squares[sq] == king_piece)
            return is_attacked(st, sq_row(sq), sq_col(sq), !checking_white);
    return false;
}

// ──────────────────────────────────────────────────────────────────────────────
// Move generation (unchanged from engine.cpp)
// ──────────────────────────────────────────────────────────────────────────────

void get_pawn_moves(const BoardState &st, int r, int c, int piece, std::vector<uint32_t> &out)
{
    bool is_w = is_white(piece);
    int direction = is_w ? 1 : -1;
    int promotion_rank = is_w ? 5 : 0;
    auto at = [&](int rr, int cc) -> int
    {
        if (!in_bounds(rr, cc)) return EMPTY;
        return st.squares[sq_index(rr, cc)];
    };
    auto add_move = [&](int nr, int nc)
    {
        int dest_sq = sq_index(nr, nc);
        if (nr == promotion_rank)
        {
            if (is_w)
            {
                if (st.graveyard[W_QUEEN]  > 0) out.push_back(encode_move(sq_index(r, c), dest_sq, piece, W_QUEEN));
                if (st.graveyard[W_BISHOP] > 0) out.push_back(encode_move(sq_index(r, c), dest_sq, piece, W_BISHOP));
                if (st.graveyard[W_KNIGHT] > 0) out.push_back(encode_move(sq_index(r, c), dest_sq, piece, W_KNIGHT));
            }
            else
            {
                if (st.graveyard[B_QUEEN]  > 0) out.push_back(encode_move(sq_index(r, c), dest_sq, piece, B_QUEEN));
                if (st.graveyard[B_BISHOP] > 0) out.push_back(encode_move(sq_index(r, c), dest_sq, piece, B_BISHOP));
                if (st.graveyard[B_KNIGHT] > 0) out.push_back(encode_move(sq_index(r, c), dest_sq, piece, B_KNIGHT));
            }
        }
        else
        {
            out.push_back(encode_move(sq_index(r, c), dest_sq, piece, 0));
        }
    };

    int nr = r + direction;
    if (in_bounds(nr, c) && at(nr, c) == EMPTY)
        add_move(nr, c);
    for (int dc : {-1, 1})
    {
        int nc = c + dc;
        if (in_bounds(nr, nc))
        {
            int target = at(nr, nc);
            if (target != EMPTY && !same_side(piece, target))
                add_move(nr, nc);
        }
    }
}

void get_knight_moves(const BoardState &st, int r, int c, int piece, std::vector<uint32_t> &out)
{
    auto at = [&](int rr, int cc) -> int
    {
        if (!in_bounds(rr, cc)) return EMPTY;
        return st.squares[sq_index(rr, cc)];
    };
    for (auto &d : KNIGHT_DIRS)
    {
        int nr = r + d[0], nc = c + d[1];
        if (!in_bounds(nr, nc)) continue;
        int target = at(nr, nc);
        if (target == EMPTY || !same_side(piece, target))
            out.push_back(encode_move(sq_index(r, c), sq_index(nr, nc), piece, 0));
    }
}

void get_sliding_moves(const BoardState &st, int r, int c, int piece,
                       const int dirs[][2], int dir_count,
                       std::vector<uint32_t> &out)
{
    auto at = [&](int rr, int cc) -> int
    {
        if (!in_bounds(rr, cc)) return EMPTY;
        return st.squares[sq_index(rr, cc)];
    };
    int from_sq = sq_index(r, c);
    for (int i = 0; i < dir_count; ++i)
    {
        int dr = dirs[i][0], dc = dirs[i][1];
        int nr = r + dr, nc = c + dc;
        while (in_bounds(nr, nc))
        {
            int target = at(nr, nc);
            int to_sq = sq_index(nr, nc);
            if (target == EMPTY)
            {
                out.push_back(encode_move(from_sq, to_sq, piece, 0));
            }
            else
            {
                if (!same_side(piece, target))
                    out.push_back(encode_move(from_sq, to_sq, piece, 0));
                break;
            }
            nr += dr; nc += dc;
        }
    }
}

void get_king_moves(const BoardState &st, int r, int c, int piece, std::vector<uint32_t> &out)
{
    auto at = [&](int rr, int cc) -> int
    {
        if (!in_bounds(rr, cc)) return EMPTY;
        return st.squares[sq_index(rr, cc)];
    };
    int from_sq = sq_index(r, c);
    for (int dr = -1; dr <= 1; ++dr)
        for (int dc = -1; dc <= 1; ++dc)
        {
            if (dr == 0 && dc == 0) continue;
            int nr = r + dr, nc = c + dc;
            if (!in_bounds(nr, nc)) continue;
            int target = at(nr, nc);
            if (target == EMPTY || !same_side(piece, target))
                out.push_back(encode_move(from_sq, sq_index(nr, nc), piece, 0));
        }
}

void generate_pseudo_moves(const BoardState &st, bool playing_white, std::vector<uint32_t> &out)
{
    out.clear();
    for (int r = 0; r < BOARD_SIZE; ++r)
        for (int c = 0; c < BOARD_SIZE; ++c)
        {
            int sq = sq_index(r, c);
            int piece = st.squares[sq];
            if (piece == EMPTY) continue;
            if (is_white(piece) != playing_white) continue;
            switch (piece)
            {
            case W_PAWN: case B_PAWN:   get_pawn_moves(st, r, c, piece, out); break;
            case W_KNIGHT: case B_KNIGHT: get_knight_moves(st, r, c, piece, out); break;
            case W_BISHOP: case B_BISHOP: get_sliding_moves(st, r, c, piece, DIAG_DIRS, 4, out); break;
            case W_QUEEN: case B_QUEEN:
                get_sliding_moves(st, r, c, piece, DIAG_DIRS, 4, out);
                get_sliding_moves(st, r, c, piece, ORTHO_DIRS, 4, out);
                break;
            case W_KING: case B_KING:   get_king_moves(st, r, c, piece, out); break;
            default: break;
            }
        }
}

void generate_legal_moves(BoardState &st, bool playing_white, std::vector<uint32_t> &out)
{
    std::vector<uint32_t> pseudo;
    generate_pseudo_moves(st, playing_white, pseudo);
    out.clear();

    int our_king = playing_white ? W_KING : B_KING;
    int king_r = -1, king_c = -1;
    for (int sq = 0; sq < 36; ++sq)
        if (st.squares[sq] == our_king) { king_r = sq_row(sq); king_c = sq_col(sq); break; }
    if (king_r == -1) { out.clear(); return; }

    for (uint32_t m : pseudo)
    {
        int from = move_from(m), to = move_to(m);
        int piece = move_piece(m), promo = move_promo(m);
        int captured = st.squares[to];

        hash_piece(st, from, piece);
        if (captured != EMPTY) hash_piece(st, to, captured);

        st.squares[from] = EMPTY;
        st.squares[to] = promo ? promo : piece;

        if (captured != EMPTY)
        {
            st.bitboards[captured] &= ~(uint64_t(1) << to);
            if (is_white(captured)) st.white_occupancy &= ~(uint64_t(1) << to);
            else                    st.black_occupancy &= ~(uint64_t(1) << to);
            st.all_occupancy &= ~(uint64_t(1) << to);
        }
        st.bitboards[piece] &= ~(uint64_t(1) << from);
        st.bitboards[promo ? promo : piece] |= (uint64_t(1) << to);
        if (is_white(piece)) { st.white_occupancy &= ~(uint64_t(1) << from); st.white_occupancy |= (uint64_t(1) << to); }
        else                 { st.black_occupancy &= ~(uint64_t(1) << from); st.black_occupancy |= (uint64_t(1) << to); }
        st.all_occupancy = st.white_occupancy | st.black_occupancy;

        int kr = (piece == our_king) ? sq_row(to) : king_r;
        int kc = (piece == our_king) ? sq_col(to) : king_c;
        bool king_in_check = is_attacked(st, kr, kc, !playing_white);

        st.squares[from] = piece;
        st.squares[to]   = captured;
        st.bitboards[piece] |= (uint64_t(1) << from);
        st.bitboards[promo ? promo : piece] &= ~(uint64_t(1) << to);
        if (captured != EMPTY) st.bitboards[captured] |= (uint64_t(1) << to);
        if (is_white(piece)) { st.white_occupancy &= ~(uint64_t(1) << to); st.white_occupancy |= (uint64_t(1) << from); }
        else                 { st.black_occupancy &= ~(uint64_t(1) << to); st.black_occupancy |= (uint64_t(1) << from); }
        if (captured != EMPTY) {
            if (is_white(captured)) st.white_occupancy |= (uint64_t(1) << to);
            else                    st.black_occupancy |= (uint64_t(1) << to);
        }
        st.all_occupancy = st.white_occupancy | st.black_occupancy;

        hash_piece(st, from, piece);
        if (captured != EMPTY) hash_piece(st, to, captured);

        if (!king_in_check) out.push_back(m);
    }
}

// ──────────────────────────────────────────────────────────────────────────────
// Phase 4c: Make / Unmake with accumulator updates
// ──────────────────────────────────────────────────────────────────────────────

struct UndoInfo
{
    int captured_piece;
    int from_sq;
    int to_sq;
    int piece;
    int promo;
    int grave_before[11];
    uint64_t hash_before;
    bool white_to_move_before;
    // Both accumulators saved before the move (2 × 256 × 2 bytes = 1 KB)
    int16_t acc_before[2][256];
};

void make_move(BoardState &st, uint32_t m, UndoInfo &ui)
{
    ui.from_sq = move_from(m);
    ui.to_sq   = move_to(m);
    ui.piece   = move_piece(m);
    ui.promo   = move_promo(m);
    ui.captured_piece = st.squares[ui.to_sq];
    ui.hash_before = st.hash;
    ui.white_to_move_before = st.white_to_move;
    std::memcpy(ui.grave_before, st.graveyard, sizeof(st.graveyard));

    // Save both accumulators (2 × 256 int16 = 1 KB total)
    std::memcpy(ui.acc_before, st.accumulator, sizeof(st.accumulator));

    int from     = ui.from_sq;
    int to       = ui.to_sq;
    int piece    = ui.piece;
    int promo    = ui.promo;
    int captured = ui.captured_piece;

    hash_side(st);
    st.white_to_move = !st.white_to_move;

    hash_piece(st, from, piece);
    if (captured != EMPTY) hash_piece(st, to, captured);

    st.squares[from] = EMPTY;
    st.squares[to]   = promo ? promo : piece;

    // ── Dual accumulator incremental update ──────────────────────────────────
    int arriving_piece = promo ? promo : piece;
    // 1. Remove moving piece from source (both perspectives)
    acc_sub_piece(st, sq_row(from), sq_col(from), piece);
    // 2. Add arriving piece at destination (both perspectives)
    acc_add_piece(st, sq_row(to), sq_col(to), arriving_piece);
    // 3. Remove captured piece (both perspectives)
    if (captured != EMPTY)
        acc_sub_piece(st, sq_row(to), sq_col(to), captured);
    // ── End accumulator update ────────────────────────────────────────────────

    // Bitboards / occupancy
    st.bitboards[piece] &= ~(uint64_t(1) << from);
    if (promo) st.bitboards[promo]  |= (uint64_t(1) << to);
    else       st.bitboards[piece]  |= (uint64_t(1) << to);

    if (is_white(piece))
    {
        st.white_occupancy &= ~(uint64_t(1) << from);
        st.white_occupancy |=  (uint64_t(1) << to);
    }
    else
    {
        st.black_occupancy &= ~(uint64_t(1) << from);
        st.black_occupancy |=  (uint64_t(1) << to);
    }
    if (captured != EMPTY)
    {
        st.bitboards[captured] &= ~(uint64_t(1) << to);
        if (is_white(captured)) st.white_occupancy &= ~(uint64_t(1) << to);
        else                    st.black_occupancy &= ~(uint64_t(1) << to);
    }
    st.all_occupancy = st.white_occupancy | st.black_occupancy;

    // Graveyard
    if (captured != EMPTY &&
        (captured == W_QUEEN || captured == W_BISHOP || captured == W_KNIGHT ||
         captured == B_QUEEN || captured == B_BISHOP || captured == B_KNIGHT))
    {
        int before = st.graveyard[captured];
        int after  = before + 1;
        st.graveyard[captured] = after;
        hash_grave(st, captured, before, after);
    }
    if (promo &&
        (promo == W_QUEEN || promo == W_BISHOP || promo == W_KNIGHT ||
         promo == B_QUEEN || promo == B_BISHOP || promo == B_KNIGHT))
    {
        int before = st.graveyard[promo];
        int after  = std::max(0, before - 1);
        st.graveyard[promo] = after;
        hash_grave(st, promo, before, after);
    }
}

void unmake_move(BoardState &st, const UndoInfo &ui)
{
    int from     = ui.from_sq;
    int to       = ui.to_sq;
    int piece    = ui.piece;
    int promo    = ui.promo;
    int captured = ui.captured_piece;
    int arriving = promo ? promo : piece;

    // Restore scalar state
    st.hash = ui.hash_before;
    st.white_to_move = ui.white_to_move_before;
    std::memcpy(st.graveyard, ui.grave_before, sizeof(st.graveyard));

    // Restore both accumulators (1 KB memcpy – always faster than re-diffing)
    std::memcpy(st.accumulator, ui.acc_before, sizeof(st.accumulator));

    // Restore square map
    st.squares[from] = piece;
    st.squares[to]   = captured;

    // ── XOR-based bitboard restoration (no loop, no memset) ──────────────────
    uint64_t from_bb = uint64_t(1) << from;
    uint64_t to_bb   = uint64_t(1) << to;

    // Remove arriving piece from `to`, restore original piece at `from`
    st.bitboards[arriving] ^= to_bb;
    st.bitboards[piece]    ^= from_bb;

    // Occupancy: moving side toggles both squares
    if (is_white(piece)) { st.white_occupancy ^= (from_bb | to_bb); }
    else                 { st.black_occupancy ^= (from_bb | to_bb); }

    // Restore captured piece at `to`
    if (captured != EMPTY)
    {
        st.bitboards[captured] ^= to_bb;
        if (is_white(captured)) st.white_occupancy ^= to_bb;
        else                    st.black_occupancy ^= to_bb;
    }

    st.all_occupancy = st.white_occupancy | st.black_occupancy;
}

// ──────────────────────────────────────────────────────────────────────────────
// Phase 5: NNUE evaluate()
//
// Forward pass after the accumulator (which holds the S1-scaled fc1 output):
//   1. ReLU the accumulator → activated[256]  (int16_t)
//   2. fc2 dot product      → fc2_out[32]     (int32_t)
//   3. ReLU                 → fc2_act[32]     (int32_t)
//   4. fc3 dot product      → raw_logit       (int32_t)
//
// Output scaling (no sigmoid!):
//   raw_logit represents (cp / 400) * S1 * S2 * S2
//   → centipawns = raw_logit * 400 / (S1 * S2 * S2)
//
// The result is always from White's absolute perspective;
// negate when it is Black's turn so minimax works correctly.
// ──────────────────────────────────────────────────────────────────────────────

int evaluate(const BoardState &st)
{
    // Select accumulator perspective:
    //   [0] = White's absolute view   (queried when White to move)
    //   [1] = Black's canonical view  (queried when Black to move)
    // The model was trained with canonical_form, so its output always represents
    // the CURRENT PLAYER's advantage. We negate for Black to give minimax the
    // White-absolute score it expects.
    const int16_t *acc = st.accumulator[st.white_to_move ? 0 : 1];

    // ── Layer 1 ReLU ─────────────────────────────────────────────────────────
    int16_t activated[256];
    #pragma GCC unroll 8
    for (int i = 0; i < 256; ++i)
        activated[i] = (acc[i] > 0) ? acc[i] : (int16_t)0;

    // ── Layer 2: fc2 ─────────────────────────────────────────────────────────
    int32_t fc2_out[32];
    for (int j = 0; j < 32; ++j)
        fc2_out[j] = nnue::fc2_bias[j];

    for (int i = 0; i < 256; ++i)
    {
        if (activated[i] == 0) continue;   // skip zero rows for speed
        const int16_t *col = &nnue::fc2_weights[i * 32];
        #pragma GCC unroll 8
        for (int j = 0; j < 32; ++j)
            fc2_out[j] += (int32_t)activated[i] * (int32_t)col[j];
    }

    // ── Layer 2 ReLU ─────────────────────────────────────────────────────────
    #pragma GCC unroll 8
    for (int j = 0; j < 32; ++j)
        if (fc2_out[j] < 0) fc2_out[j] = 0;

    // ── Layer 3: fc3 (scalar output) ─────────────────────────────────────────
    int64_t raw_logit = (int64_t)nnue::fc3_bias;
    #pragma GCC unroll 8
    for (int j = 0; j < 32; ++j)
        raw_logit += (int64_t)fc2_out[j] * (int64_t)nnue::fc3_weights[j];

    // raw_logit ≈ (cp / 400) * S1 * S2 * S2   (see nnue_weights.h header)
    constexpr int64_t DENOM = (int64_t)nnue::SCALE_S1 * nnue::SCALE_S2 * nnue::SCALE_S2;
    int cp = (int)(raw_logit * 400LL / DENOM);

    // Model output = "current player advantage"
    // → negate for Black so minimax always sees White-absolute centipawns
    return st.white_to_move ? cp : -cp;
}

